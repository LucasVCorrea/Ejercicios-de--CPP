#include <iostream>

using namespace std;

int main()
{
    int ingreso;
    int contador=1;
    int i;
    cout<<"Ingrese un numero para calcular su factorial: ";cin>>ingreso;
    for(i=ingreso;i>=1;i--)
        {
            cout << i << endl;
            contador*=i;
            
        }
        
    cout<<"Factorial de "<<ingreso<<" = "<<contador<<endl;
    return 0;
}
